/* USER CODE BEGIN 0 */
void measure_temperature(void* pvParameters)
{
  TickType_t xDelay = 1000 / portTICK_PERIOD_MS;
  for(;;)
  {
    uint32_t sum = 0;
    for(int i = 0; i < 100; i++) {
      if (HAL_ADC_PollForConversion(&hadc1, 100) == HAL_OK) {

        sum = sum + HAL_ADC_GetValue(&hadc1);

      }
        HAL_ADC_Start(&hadc1);

    }

    ts_val =  map(70, 100, (sum / 100.00));
    measure_count++;
    vTaskDelay(xDelay);
  }

}


int map(int  ymin, int ymax, int x) {
  if(ymin < ymax) {
    float ynormMax = ymax - ymin ;
    float y = (ynormMax / 4095.00) * ((float) x);
    return y + ymin;
  } else {
    return -1;
  }
}

void fan_controller(void* pvParameters) {
  TickType_t xDelay = 1000 / portTICK_PERIOD_MS;
  for(;;)
  {
    if(ts_val > 85) {
        HAL_GPIO_WritePin(FAN_GPIO_Port, FAN_Pin, GPIO_PIN_SET);
    }  else {
        HAL_GPIO_WritePin(FAN_GPIO_Port, FAN_Pin, GPIO_PIN_RESET);
    }
    vTaskDelay(xDelay);
  }
}

